package com.mfan.entity;

public class Movies {
	private int movieId;
	private String movieName;
	private String movieRelaseDate;
	private String language;
	private String movieIdentifier;
	
	public int getMovieId() {
		return movieId;
	}
	public void setMovieId(int movieId) {
		this.movieId = movieId;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getMovieRelaseDate() {
		return movieRelaseDate;
	}
	public void setMovieRelaseDate(String movieRelaseDate) {
		this.movieRelaseDate = movieRelaseDate;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public String getMovieIdentifier() {
		return movieIdentifier;
	}
	public void setMovieIdentifier(String movieIdentifier) {
		this.movieIdentifier = movieIdentifier;
	}
	
	
}
